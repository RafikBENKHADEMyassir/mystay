/** Build and sort the full country list once. */
function buildCountries(): PhoneCountry[] {
  const list: PhoneCountry[] = [];

  for (const iso of getCountries()) {
    const name = COUNTRY_NAMES[iso] ?? iso;
    list.push({
      iso,
      name,
      flag: isoToFlag(iso),
      callingCode: getCountryCallingCode(iso),
    });
  }

  list.sort((a, b) => a.name.localeCompare(b.name));
  return list;
}

/** All countries sorted alphabetically by name. */
export const ALL_COUNTRIES: PhoneCountry[] = buildCountries();

/** Look up a country by its ISO code. */
export function getCountryByIso(iso: string): PhoneCountry | undefined {
  return ALL_COUNTRIES.find((c) => c.iso === iso);
}

/** Get the dial string (e.g. "+33") from an ISO code. */
export function getDialCode(iso: string): string {
  const country = getCountryByIso(iso);
  return country ? `+${country.callingCode}` : "";
}
